def _gen_subjects_dir(inputs=None, stdout=None, stderr=None, output_dir=None):
    return output_dir


def _gen_filename(name, inputs=None, stdout=None, stderr=None, output_dir=None):
    if name == "subjects_dir":
        return _gen_subjects_dir(
            inputs=inputs, stdout=stdout, stderr=stderr, output_dir=output_dir
        )
    return None


def _list_outputs(inputs=None, stdout=None, stderr=None, output_dir=None):
    """
    See io.FreeSurferSource.outputs for the list of outputs returned
    """
    if inputs.subjects_dir is not attrs.NOTHING:
        subjects_dir = inputs.subjects_dir
    else:
        subjects_dir = _gen_subjects_dir(
            inputs=inputs, stdout=stdout, stderr=stderr, output_dir=output_dir
        )

    if inputs.hemi is not attrs.NOTHING:
        hemi = inputs.hemi
    else:
        hemi = "both"

    outputs = {}

    outputs.update(
        FreeSurferSource(
            subject_id=inputs.subject_id, subjects_dir=subjects_dir, hemi=hemi
        )._list_outputs()
    )
    outputs["subject_id"] = inputs.subject_id
    outputs["subjects_dir"] = subjects_dir
    return outputs
